package culturelandFront.vo;

import org.apache.ibatis.type.Alias;

import culturelandFront.core.abstr.NdnAbstractVO;

@Alias("historyAdminloginVO")
public class HistoryAdminloginVO extends NdnAbstractVO {

	private int seq;
	private String regdate;
	private String ip;
	private String logtime;
	private String adminid;
	private String action;
	private String login_time;
	
	
	
	public String getLogin_time() {
		return login_time;
	}

	public void setLogin_time(String login_time) {
		this.login_time = login_time;
	}

	public HistoryAdminloginVO() {
	}
	
	public HistoryAdminloginVO(String adminid){
		this.adminid = adminid;
	}

	public int getSeq() {
		return seq;
	}

	public void setSeq(int seq) {
		this.seq = seq;
	}

	public String getRegdate() {
		return regdate;
	}

	public void setRegdate(String regdate) {
		this.regdate = regdate;
	}

	public String getIp() {
		return ip;
	}

	public void setIp(String ip) {
		this.ip = ip;
	}

	public String getLogtime() {
		return logtime;
	}

	public void setLogtime(String logtime) {
		this.logtime = logtime;
	}

	public String getAdminid() {
		return adminid;
	}

	public void setAdminid(String adminid) {
		this.adminid = adminid;
	}

	public String getAction() {
		return action;
	}

	public void setAction(String action) {
		this.action = action;
	}

}
